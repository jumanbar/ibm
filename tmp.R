source('ibm.R')

ex <- seq(-2, 1, len=24)
Ms <- 10 ^ ex

run <- ibm(landsDist_ = 4, levelSeeds = 0,
           mpd0 = 6, sizeMode = "random", als0=5e6,
           trs0 = 0.1, yield = 2000, tfinal=1300,
           M = Ms[1], saveRecord=FALSE)

save(run, file='../saves/corrida-M=0.01.RData')

